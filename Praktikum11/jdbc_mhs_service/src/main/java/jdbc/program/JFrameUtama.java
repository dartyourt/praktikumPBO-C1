    private void jButtonEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditActionPerformed
        try{
            int id = Integer.parseInt(jTextFieldID2.getText().trim());
            String nama = jTextFieldNama2.getText().trim();
            
            if(nama.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Nama jan kosong");
                return;
            }

            Mahasiswa mhs = service.getById(id);
            if (mhs == null) {
                JOptionPane.showMessageDialog(this, "Mahasiswa dengan ID " + id + " tidak ditemukan!");
                return;
            }
            mhs.setNama(nama);
            service.update(mhs);

            tampilkanSemua();
        }
        catch (NumberFormatException e){
            JOptionPane.showMessageDialog(this, "ID harus angka");
            
        }
    }//GEN-LAST:event_jButtonEditActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        try {
            // Validasi ID kosong
            if (jTextFieldID3.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "ID tidak boleh kosong!");
                return;
            }
               
            int id = Integer.parseInt(jTextFieldID3.getText().trim());
            
            // Periksa apakah ID ada di database
            Mahasiswa mhs = service.getById(id);
            if (mhs == null) {
                JOptionPane.showMessageDialog(this, "Mahasiswa dengan ID " + id + " tidak ditemukan!");
                return;
            }
            
            // Konfirmasi penghapusan
            int confirm = JOptionPane.showConfirmDialog(this, 
                    "Yakin hapus mahasiswa dengan nama: " + mhs.getNama() + "?",
                    "Konfirmasi", JOptionPane.YES_NO_OPTION);
            
            if (confirm == JOptionPane.YES_OPTION) {
                // Lakukan penghapusan
                service.delete(id);
                tampilkanSemua();
                JOptionPane.showMessageDialog(this, "Data berhasil dihapus!");
            }
        }
        catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "ID nya angka, pls");
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

