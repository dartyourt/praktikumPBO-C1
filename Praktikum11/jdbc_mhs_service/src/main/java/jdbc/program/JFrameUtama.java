    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        String nama = jTextFieldNama.getText().trim();
        if (nama.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Nama jangan kosong!");
            return;
        }
        
        int newId = 1;
        if (!modelID.isEmpty()){
            int lastId = Integer.parseInt(modelID.getElementAt(modelID.size()-1));
            newId = lastId + 1;
        }
        Mahasiswa mhs = new Mahasiswa(newId, nama);
        service.add(mhs);
        tampilkanSemua();
        jTextFieldNama.setText("");
        JOptionPane.showMessageDialog(this, "Mhs telah ditambahkan!");
    }//GEN-LAST:event_jButtonAddActionPerformed

